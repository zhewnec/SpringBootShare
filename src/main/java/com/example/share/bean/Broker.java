package com.example.share.bean;

public class Broker {
    private int id;
    private String salesName;
    private String salesCode;
    private String shareName;
    private String shareCode;
    private String transactDate;
    private String BMoney;
    private String SMoney;
    private String PBuy;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSalesName() {
        return salesName;
    }

    public void setSalesName(String salesName) {
        this.salesName = salesName;
    }

    public String getSalesCode() {
        return salesCode;
    }

    public void setSalesCode(String salesCode) {
        this.salesCode = salesCode;
    }

    public String getShareName() {
        return shareName;
    }

    public void setShareName(String shareName) {
        this.shareName = shareName;
    }

    public String getShareCode() {
        return shareCode;
    }

    public void setShareCode(String shareCode) {
        this.shareCode = shareCode;
    }

    public String getTransactDate() {
        return transactDate;
    }

    public void setTransactDate(String transactDate) {
        this.transactDate = transactDate;
    }

    public String getBMoney() {
        return BMoney;
    }

    public void setBMoney(String BMoney) {
        this.BMoney = BMoney;
    }

    public String getSMoney() {
        return SMoney;
    }

    public void setSMoney(String SMoney) {
        this.SMoney = SMoney;
    }

    public String getPBuy() {
        return PBuy;
    }

    public void setPBuy(String PBuy) {
        this.PBuy = PBuy;
    }


}
